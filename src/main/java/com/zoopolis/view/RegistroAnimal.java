    private void capturarDatos(){
       if(rbtnAnimalDomestico.isSelected()== true){
           AnimalDomestico ad = new AnimalDomestico(txtRaza.getText(), txtColor.getText(),"IMAGEN BONITA", txtEspecie.getText(), btngSexo.getSelection().toString(), Integer.parseInt(spnEdad.getValue().toString()), txADescripcion.getText());
           Zoopolis.animalesDomestico.add(ad);
           JOptionPane.showMessageDialog(null, "SE REGISTRO EL ANIMAL DOMESTICO", "EXISTOSA", JOptionPane.OK_OPTION);
        }else{
           AnimalSalvaje as = new AnimalSalvaje(cmbPeligrosidad.getSelectedItem().toString(), "IMAGEN RUDA", txtEspecie.getText(), btngSexo.getSelection().toString(), Integer.parseInt(spnEdad.getValue().toString()), txADescripcion.getText(), txtHabitat.getText()); 
           Zoopolis.animalesSalvaje.add(as);
           JOptionPane.showMessageDialog(null, "SE REGISTRO EL ANIMAL DOMESTICO", "EXISTOSA", JOptionPane.OK_OPTION);
       }
       
    }
    
    public void limpiarCampos(){
        rbtnAnimalDomestico.setSelected(false);
        rbtnAnimalSalvaje.setSelected(false);
        txtEspecie.setText("");
        btngSexo.clearSelection();
        spnEdad.setValue(0);
        txtRaza.setText("");
        txtColor.setText("");
        txtHabitat.setText("");
        cmbPeligrosidad.setSelectedIndex(0);
        txADescripcion.setText("");
        lblFoto.setIcon(new javax.swing.ImageIcon(getClass().getResource("/assets/Foto.png")));
    }
    
    
    
    
    
