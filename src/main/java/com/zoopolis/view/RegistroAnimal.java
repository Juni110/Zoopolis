        int i = 0;
        if(jf.showOpenDialog(this) == JFileChooser.APPROVE_OPTION){
            rsdragdropfiles.RSDragDropFiles.setCopiar(jf.getSelectedFile().toString(), "src/main/resources/assets/animal"+i);
            i++;
        }
